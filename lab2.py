# Пользователь вводит число от 1 до 100
level = int(input("Введите уровень угрозы (1-100): "))

# Проверяем, к какому диапазону относится число
if 1 <= level <= 30:
    # Если от 1 до 30 — низкий уровень
    print("Низкий уровень угрозы")
elif 31 <= level <= 70:
    # Если от 31 до 70 — средний уровень
    print("Средний уровень угрозы")
elif 71 <= level <= 100:
    # Если от 71 до 100 — критический уровень
    print("Критический уровень угрозы")
else:
    # Если число вне диапазона — ошибка
    print("Ошибка ввода")
