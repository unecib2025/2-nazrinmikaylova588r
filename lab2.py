# Пользователь вводит контрольные суммы (для проверки целостности файла)
checksum_original = input("Оригинальная контрольная сумма: ")
checksum_current = input("Текущая контрольная сумма: ")

# Если суммы совпадают — файл не изменён, иначе — повреждён
status = "Файл не изменён" if checksum_original == checksum_current else "Файл повреждён"

# Вывод результата
print(status)
