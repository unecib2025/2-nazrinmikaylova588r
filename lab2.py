# Пользователь вводит код события
event_code = input("Введите код события (ERR/WRN/INF): ")

# Проверяем значение кода
match event_code:
    case "ERR":
        print("Ошибка системы")  # Ошибка
    case "WRN":
        print("Предупреждение")  # Предупреждение
    case "INF":
        print("Информационное сообщение")  # Информация
    case _:
        print("Неизвестный код события")  # Всё остальное

