# Пользователь вводит состояние сервера
status = input("Введите статус сервера (online/offline): ")

# Проверяем, онлайн ли сервер
if status == "online":
    # Если онлайн — связь есть
    print("Связь установлена")
else:
    # Если нет — связь потеряна
    print("Связь потеряна")
